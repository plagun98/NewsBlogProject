        public int GetViewsCountByUser(string id)
        {
            int articlesCount = (from a in db.Articles where a.UserID == id select a).Sum(x => (int?)x.ViewCount) ?? 0;

            return articlesCount;
        }

        public int GetCommentsCountByUser(string id)
        {
            int commentsCount = (from a in db.Articles where a.UserID == id select a).Sum(x => (int?)x.CommentsCount) ?? 0;

            return commentsCount;
        }


